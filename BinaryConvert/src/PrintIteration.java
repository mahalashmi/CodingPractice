class PrintIteration
{
    public static void main(String[] args)
    {
        //PrintIteration p1=new PrintIteration();
        printmethod(1,100);
    }
    
          static void printmethod(int start,int end)
          {
           while(start<=end)
            {
                System.out.println(start);
                start ++;
             }
    
           }
}